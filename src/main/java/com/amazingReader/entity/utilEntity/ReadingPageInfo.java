package com.amazingReader.entity.utilEntity;

import com.amazingReader.entity.Book;
import com.amazingReader.entity.Chapter;
import com.amazingReader.entity.ChapterAdditionRemarks;
import com.amazingReader.entity.Writer;

import java.util.List;

public class ReadingPageInfo {
    private Chapter chapter;//章节内容

    private List<Chapter> menu;//目录

    private Book book;//该书信息

    private ChapterAdditionRemarks chapterAdditionRemarks;//作者有话说

    private Writer writer;//作者信息

    public Writer getWriter() {
        return writer;
    }

    public void setWriter(Writer writer) {
        this.writer = writer;
    }

    public Chapter getChapter() {
        return chapter;
    }

    public void setChapter(Chapter chapter) {
        this.chapter = chapter;
    }

    public List<Chapter> getMenu() {
        return menu;
    }

    public void setMenu(List<Chapter> menu) {
        this.menu = menu;
    }

    public Book getBook() {
        return book;
    }

    public void setBook(Book book) {
        this.book = book;
    }

    public ChapterAdditionRemarks getChapterAdditionRemarks() {
        return chapterAdditionRemarks;
    }

    public void setChapterAdditionRemarks(ChapterAdditionRemarks chapterAdditionRemarks) {
        this.chapterAdditionRemarks = chapterAdditionRemarks;
    }

    @Override
    public String toString() {
        return "ReadingPageInfo{" +
                "chapter=" + chapter +
                ", menu=" + menu +
                ", book=" + book +
                ", chapterAdditionRemarks=" + chapterAdditionRemarks +
                ", writer=" + writer +
                '}';
    }
}
