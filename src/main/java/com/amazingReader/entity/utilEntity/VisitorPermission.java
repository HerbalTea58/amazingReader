package com.amazingReader.entity.utilEntity;

public class VisitorPermission {
    private boolean bookShelf;
    private boolean readingRecord;
    private boolean readerId;

    public boolean isBookShelf() {
        return bookShelf;
    }

    public void setBookShelf(boolean bookShelf) {
        this.bookShelf = bookShelf;
    }

    public boolean isReadingRecord() {
        return readingRecord;
    }

    public void setReadingRecord(boolean readingRecord) {
        this.readingRecord = readingRecord;
    }

    public boolean isReaderId() {
        return readerId;
    }

    public void setReaderId(boolean readerId) {
        this.readerId = readerId;
    }
}
