package com.amazingReader.entity;

import java.util.Date;

public class ChapterCheck {
    private Boolean checkChapterFrom;

    private Integer checkChapterNum;

    private Integer checkChapterBookId;

    private String checkChapterName;

    private Integer checkChapterWordNum;

    private Integer checkChapterPrice;

    private Boolean checkChapterStatu;

    private String checkChapterContent;

    private Date checkRefreshTime;

    private Boolean checkChapterAddtion;

    private String checkChapterAdditionCharacter;

    public ChapterCheck() {
    }

    public Boolean getCheckChapterFrom() {
        return checkChapterFrom;
    }

    public void setCheckChapterFrom(Boolean checkChapterFrom) {
        this.checkChapterFrom = checkChapterFrom;
    }

    public Integer getCheckChapterNum() {
        return checkChapterNum;
    }

    public void setCheckChapterNum(Integer checkChapterNum) {
        this.checkChapterNum = checkChapterNum;
    }

    public Integer getCheckChapterBookId() {
        return checkChapterBookId;
    }

    public void setCheckChapterBookId(Integer checkChapterBookId) {
        this.checkChapterBookId = checkChapterBookId;
    }

    public String getCheckChapterName() {
        return checkChapterName;
    }

    public void setCheckChapterName(String checkChapterName) {
        this.checkChapterName = checkChapterName;
    }

    public Integer getCheckChapterWordNum() {
        return checkChapterWordNum;
    }

    public void setCheckChapterWordNum(Integer checkChapterWordNum) {
        this.checkChapterWordNum = checkChapterWordNum;
    }

    public Integer getCheckChapterPrice() {
        return checkChapterPrice;
    }

    public void setCheckChapterPrice(Integer checkChapterPrice) {
        this.checkChapterPrice = checkChapterPrice;
    }

    public Boolean getCheckChapterStatu() {
        return checkChapterStatu;
    }

    public void setCheckChapterStatu(Boolean checkChapterStatu) {
        this.checkChapterStatu = checkChapterStatu;
    }

    public String getCheckChapterContent() {
        return checkChapterContent;
    }

    public void setCheckChapterContent(String checkChapterContent) {
        this.checkChapterContent = checkChapterContent;
    }

    public Date getCheckRefreshTime() {
        return checkRefreshTime;
    }

    public void setCheckRefreshTime(Date checkRefreshTime) {
        this.checkRefreshTime = checkRefreshTime;
    }

    public Boolean getCheckChapterAddtion() {
        return checkChapterAddtion;
    }

    public void setCheckChapterAddtion(Boolean checkChapterAddtion) {
        this.checkChapterAddtion = checkChapterAddtion;
    }

    public String getCheckChapterAdditionCharacter() {
        return checkChapterAdditionCharacter;
    }

    public void setCheckChapterAdditionCharacter(String checkChapterAdditionCharacter) {
        this.checkChapterAdditionCharacter = checkChapterAdditionCharacter;
    }

    @Override
    public String toString() {
        return "ChapterCheck{" +
                "checkChapterFrom=" + checkChapterFrom +
                ", checkChapterNum=" + checkChapterNum +
                ", checkChapterBookId=" + checkChapterBookId +
                ", checkChapterName='" + checkChapterName + '\'' +
                ", checkChapterWordNum=" + checkChapterWordNum +
                ", checkChapterPrice=" + checkChapterPrice +
                ", checkChapterStatu=" + checkChapterStatu +
                ", checkChapterContent='" + checkChapterContent + '\'' +
                ", checkRefreshTime=" + checkRefreshTime +
                ", checkChapterAddtion=" + checkChapterAddtion +
                ", checkChapterAdditionCharacter='" + checkChapterAdditionCharacter + '\'' +
                '}';
    }
}
